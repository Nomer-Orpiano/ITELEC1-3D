package main;

class Mainc {

	public static void main(String[] args) {

		    // basic if else arithmetic 
		    
		    int a = 8;
	        int b = 4;
	        int result = 0;
	        char op = '+';

	        if (op == '+') {
	            result = a + b;
	            System.out.println("Addition: " + result);
	        } else if (op == '-') {
	            result = a - b;
	            System.out.println("Subtraction: " + result);
	}

}

}